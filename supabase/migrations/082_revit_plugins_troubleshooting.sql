## 🔍 Если автоматизация не засчиталась — проверьте

1. **Плагин входит в список учитываемых.** Кристаллы начисляются только за плагины из раздела «Какие плагины учитываются» выше. Запуски других плагинов пока не отслеживаются.

2. **Плагины установлены через InstallationManager, и вы авторизованы в IM** под корпоративной почтой (`@enecagroup.com`). Только плагины, установленные и авторизованные через корпоративный InstallationManager, отправляют данные о пользователе на сервер. Если плагин скопирован вручную, установлен иначе или вход выполнен под другой почтой — запуски не привяжутся к вашему аккаунту.

3. **Плагин актуальной версии.** Старые версии некоторых плагинов не передают данные о пользователе. Обновите плагины через InstallationManager.

4. **Запуск был в рабочий день.** Запуски в субботу и воскресенье не начисляют кристаллы. Исключение — рабочие субботы по производственному календарю.

5. **Кристаллы появляются на следующее утро.** Система синхронизируется ночью — если вы запустили плагин сегодня, начисление придёт завтра утром.

Если все пункты 1–5 выполнены, а запуски всё равно не засчитываются, причина почти наверняка одна: на вашем компьютере стоит блокировка интернет-соединения, и плагин не может отправить данные на сервер. Обратитесь к системному администратору.

Если и это не помогло — сообщите о проблеме через кнопку «Обратная связь» на странице, указав дату и название плагина.

